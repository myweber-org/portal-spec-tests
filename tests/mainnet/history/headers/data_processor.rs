use std::collections::HashMap;

#[derive(Debug, Clone)]
pub struct DataRecord {
    id: u64,
    values: Vec<f64>,
    metadata: HashMap<String, String>,
}

impl DataRecord {
    pub fn new(id: u64, values: Vec<f64>) -> Self {
        Self {
            id,
            values,
            metadata: HashMap::new(),
        }
    }

    pub fn add_metadata(&mut self, key: String, value: String) {
        self.metadata.insert(key, value);
    }

    pub fn validate(&self) -> Result<(), String> {
        if self.id == 0 {
            return Err("Invalid record ID".to_string());
        }

        if self.values.is_empty() {
            return Err("Empty values array".to_string());
        }

        for &value in &self.values {
            if value.is_nan() || value.is_infinite() {
                return Err("Invalid numeric value detected".to_string());
            }
        }

        Ok(())
    }

    pub fn normalize(&mut self) {
        if self.values.is_empty() {
            return;
        }

        let sum: f64 = self.values.iter().sum();
        if sum != 0.0 {
            for value in &mut self.values {
                *value /= sum;
            }
        }
    }

    pub fn calculate_statistics(&self) -> HashMap<String, f64> {
        let mut stats = HashMap::new();

        if self.values.is_empty() {
            return stats;
        }

        let count = self.values.len() as f64;
        let sum: f64 = self.values.iter().sum();
        let mean = sum / count;

        let variance: f64 = self.values
            .iter()
            .map(|&x| (x - mean).powi(2))
            .sum::<f64>() / count;

        stats.insert("count".to_string(), count);
        stats.insert("sum".to_string(), sum);
        stats.insert("mean".to_string(), mean);
        stats.insert("variance".to_string(), variance);

        stats
    }
}

pub fn process_records(records: &mut [DataRecord]) -> Vec<Result<HashMap<String, f64>, String>> {
    records
        .iter_mut()
        .map(|record| {
            record.validate()?;
            record.normalize();
            Ok(record.calculate_statistics())
        })
        .collect()
}

